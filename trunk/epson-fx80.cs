include ascii.cs except 7C
code 7C = U+00A6 # broken bar
code 7C <- U+007C

code 00 .. 03 = U+00E0, U+00E8, U+00F9, U+00F2
code 04 .. 07 = U+00EC, U+00B0, U+00A3, U+00A1
code 08 .. 0B = U+00BF, U+00D1, U+00F1, U+00A4
code 0C .. 0F = U+20A7, U+00C5, U+00E5, U+00E7
code 10 .. 13 = U+00A7, U+00DF, U+00C6, U+00E6
code 14 .. 17 = U+00D8, U+00F8, U+00A8, U+00C4
code 18 .. 1B = U+00D6, U+00DC, U+00E4, U+00F6
code 1C .. 1F = U+00FC, U+00C9, U+00E9, U+00A5
# The code 7F is used for the stroked zero.
code 7F -> U+0030
